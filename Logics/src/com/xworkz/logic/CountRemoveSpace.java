package com.xworkz.logic;

public class CountRemoveSpace {
	public static void main(String[] args) {

		int n=32;
		if(n%2!=0) {
			System.out.println("geyg");
		}
		if(n%2==0 && n<=5 &&n>=2) {
			System.out.println("Jamn");
		}
		if(n%2==0 && n<=20 &&n>=6) {
			System.out.println("Hzhkih");
		}
		if(n%2==0 &&n>=20) {
			System.out.println("jjsgdjf");
		}
	}

}
