package com.xworkz.inheritance.Nov8;

public class BhachelorParty {
	public String location;
	public int noOfPeoples;
	public double totalAmountSpent;
	
	
	public BhachelorParty() {
		super();
	}


	public BhachelorParty(String location, int noOfPeoples, double totalAmountSpent) {
		super();
		this.location = location;
		this.noOfPeoples = noOfPeoples;
		this.totalAmountSpent = totalAmountSpent;
	}
	
	
	

}
