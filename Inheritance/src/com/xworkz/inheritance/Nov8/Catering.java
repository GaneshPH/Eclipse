package com.xworkz.inheritance.Nov8;

public class Catering {
	
	public String catererName;
	public String style;
	public boolean tasteIsGood;
	
	
	
	public Catering() {
		super();
	}



	public Catering(String catererName, String style, boolean tasteIsGood) {
		super();
		this.catererName = catererName;
		this.style = style;
		this.tasteIsGood = tasteIsGood;
	}
	
	
	

}
