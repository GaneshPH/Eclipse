package com.xworkz.inheritance.Nov8;

public class DJ {
	
	public String soundsName;
	public int price;
	public String hours;
	
	public DJ() {
		super();
	}

	public DJ(String soundsName, int price, String hours) {
		super();
		this.soundsName = soundsName;
		this.price = price;
		this.hours = hours;
	}

	
	

}
